    /**
     * 转换中文为UTF-8
     *
     * @param value 对象串
     */
    public static String Chinese2UTF8(String value) {
        char[] ch = value.toCharArray();
        String str = "";
        for (int i = 0; i < ch.length; i++) {
            if (isChinese(ch[i])) {
                try {
                    str += URLEncoder.encode(ch[i] + "", "UTF-8");
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else {
                str += ch[i];
            }
        }
        return str;
    }

